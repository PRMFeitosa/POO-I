    private void PreencherCombo() {
        FuncionarioDAO fdao = new FuncionarioDAO();
        List<Funcionario> funcs = new ArrayList<Funcionario>();
        funcs = fdao.listar();
        cbFuncionario.removeAllItems();
        for (Funcionario f : funcs) {
            cbFuncionario.addItem(f);
        }
    }

