    private void preencheTabela(List<Despesas> lista){
        double valor = 0;
        for(Despesas despesa : lista){
            valor += despesa.getValor();
        }
        tfValorTotalDespesas.setText(String.valueOf(valor));
        DespesasTableModel ctm = new DespesasTableModel(lista);
        tbDespesas.setModel(ctm);
    }

